static vsi_status op_compute
    (
    vsi_nn_node_t * self,
    vsi_nn_tensor_t ** inputs,
    vsi_nn_tensor_t ** outputs
    )
{
    vsi_status status = VSI_FAILURE;
#if (USE_DRIVER_API == 0)
    vsi_nn_kernel_info_t kernel_info = {0};
    kernel_info.resource_num = 1;
    kernel_info.resource_name = (char **)malloc(kernel_info.resource_num * sizeof(char *));
    kernel_info.resource_name[0] = "vsi_nn_kernel_dropout";
    kernel_info.type = VX_KERNEL_TYPE_VX;
    kernel_info.kernel = vx_kernel_DROPOUT_list;
    kernel_info.init_index = 1;
    kernel_info.kernel_index = 1;

    self->n = vsi_nn_RegisterClientKernelAndCreateNode(
        self->graph, &kernel_info);
    if (kernel_info.resource_name) free(kernel_info.resource_name);
    if( NULL == self->n )
    {
        return VSI_FAILURE;
    }

    if (NULL != op_compute_list[kernel_info.init_index])
    {
        status = op_compute_list[kernel_info.init_index](self, inputs, outputs);
    }
#else
    vsi_nn_tensor_t * tensor;
    vsi_nn_tensor_attr_t attr;
    float const_one = 1.0;
    float scale;
    vsi_enum overflow_policy,rounding_policy;
    vx_scalar scale_s;

    memset( &attr, 0, sizeof( vsi_nn_tensor_attr_t ) );
    attr.size[0] = 1;
    attr.size[1] = 1;
    attr.size[2] = 1;
    attr.size[3] = 1;
    attr.dim_num = inputs[0]->attr.dim_num;
    attr.is_const = TRUE;
    attr.dtype.vx_type = VSI_NN_TYPE_FLOAT32;
    tensor = vsi_nn_CreateTensorFromData( self->graph,
        (uint8_t *)&const_one, &attr );

    scale = self->nn_param.dropout.ratio;
    overflow_policy = VX_CONVERT_POLICY_SATURATE;
    rounding_policy = VX_ROUND_POLICY_TO_NEAREST_EVEN;

    scale_s = vxCreateScalar(self->graph->ctx->c, VX_TYPE_FLOAT32, &scale);
    if(!scale_s)
    {
        VSILOGE("CreateScalar fail\n");
        return VSI_FAILURE;
    }

    self->n = vxTensorMultiplyNode( self->graph->g,
        inputs[0]->t, tensor->t,
        scale_s,
        overflow_policy,
        rounding_policy,
        outputs[0]->t );

    if( NULL != self->n )
    {
        status = VSI_SUCCESS;
    }
#endif
    return status;
} /* op_compute() */

